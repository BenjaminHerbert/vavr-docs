package io.vavr;

import org.junit.Test;

public class LazyDemo {

    @Test
    public void createLazy(){
        // tag::createLazy[]
        Lazy<Double> lazy = Lazy.of(Math::random);
        lazy.isEvaluated(); // = false
        lazy.get();         // = 0.123 (random generated)
        lazy.isEvaluated(); // = true
        lazy.get();         // = 0.123 (memoized)
        // end::createLazy[]
    }
}
